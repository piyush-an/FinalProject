    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) LegalPaperJTable.getModel();
        model.setRowCount(0);
        for (WorkRequest request : LegalOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[9];
            row[0] = request;
            row[1] = request.getRequestDate();
            row[2] = request.getStatus();
            row[3] = request.getReceiver();
            row[4] = request.getMessage();
            
            row[5] = ((LegalWorkRequest) request).getPatient_name();
            row[6] = ((LegalWorkRequest) request).getDonor().getName();
            row[7] = ((LegalWorkRequest) request).getDonor().getContactNum();
            
            int i = 0;
            try {
            if(((LegalWorkRequest) request).getServiceOne())
                i++;
            if(((LegalWorkRequest) request).getServiceTwo())
                i++;
            }
            catch (Exception e) {
                
            }
            row[8] = i;
            
            model.addRow(row);
        }
    }
    

