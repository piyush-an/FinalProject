    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = LegalPaperJTable.getSelectedRow();
        if (selectedRow < 0) {
           JOptionPane.showMessageDialog(null, "No Row Selected, Please select one");
           return;
        }
        
        LegalWorkRequest request = (LegalWorkRequest) LegalPaperJTable.getValueAt(selectedRow, 0);
        
        UserAccount requestuserAccount = request.getReceiver();
        if(requestuserAccount == null){
            JOptionPane.showMessageDialog(null, "Request not assigned to you");
            return;
        }
        if (requestuserAccount.equals(userAccount)){
        LegalWorkRequestJPanel LegalWorkRequestJPanel = new LegalWorkRequestJPanel(userProcessContainer, request, business, network, userAccount);
        userProcessContainer.add("processWorkRequestJPanel", LegalWorkRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        else {
            JOptionPane.showMessageDialog(null, "Request not assigned to you");
        }
        
        
    }//GEN-LAST:event_btnProcessActionPerformed


