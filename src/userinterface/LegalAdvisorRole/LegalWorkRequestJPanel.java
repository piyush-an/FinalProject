    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String selectedStatus = ChangeStatusdd.getSelectedItem().toString();
        if (selectedStatus.equalsIgnoreCase("None")){
            JOptionPane.showMessageDialog(null, "Please select Work Status");
            return;
        }
        if(request.getStatus().equalsIgnoreCase("Assigned")){
            
//            if (!selectedStatus.equalsIgnoreCase("None")){
            request.setServiceOne(jCheckBox1.isSelected());
            request.setServiceTwo(jCheckBox2.isSelected());
            request.setStatus(selectedStatus);
            request.setMessage(txtComments.getText());
            btnHomeActionPerformed(evt);
//            }
            
        } else {
            if (!selectedStatus.equalsIgnoreCase(request.getStatus())){
            request.setStatus(selectedStatus);
            request.setMessage(txtComments.getText());
            btnHomeActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(null, "Work Status is same as current");
            }
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

