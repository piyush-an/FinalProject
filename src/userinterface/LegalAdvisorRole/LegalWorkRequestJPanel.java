    private void populateFields(){
        
        
        if(request.getStatus().equalsIgnoreCase("Assigned")){
            jCheckBox1.setEnabled(true);
            jCheckBox2.setEnabled(true);
        } else {
            jCheckBox1.setSelected(request.getServiceOne());
            jCheckBox2.setSelected(request.getServiceTwo());
            jCheckBox1.setEnabled(false);
            jCheckBox2.setEnabled(false);
        }
        if(request.getStatus().equalsIgnoreCase("Complete")){
            btnSubmit.setEnabled(false);
            ChangeStatusdd.setEnabled(false);
        }
        
        
    }

